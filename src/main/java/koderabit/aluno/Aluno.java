package koderabit.aluno;

public class Aluno {
    public String nome;
    public String matricula;
    public int ano_matricula;
    
    public Aluno(){}
    
    public Aluno(String nome, String matricula, int ano_matricula){
        this.nome = nome;
        this.matricula = matricula;
        this.ano_matricula = ano_matricula;
    }
}
